// Example 5: if-else with boolean expressions
fun main() {
    val a: Boolean = true
    val b: Boolean = false
    
    if (a && b) {
        print("Both a and b are true")
    } else if (a || b) {
        print("At least one of a or b is true")
    } else {
        print("Both a and b are false")
    }
}
